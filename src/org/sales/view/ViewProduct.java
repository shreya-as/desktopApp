    public void loadData(){
         ProductDAO pDAO=new ProductDAO();//to call create ob of that cls
        pDAO.fetchData();
        //return greko value store grnu paryo
        List<Product> productList=pDAO.fetchData();//yaa ayera bsyo rtrn greko
       clearTable();
        for(Product p:productList)
        {
            model.addRow(new Object[]{
                p.getId(),p.getName(),
                p.getPrice(),p.getQty(),
                p.getRemarks()});
            }

        
    }

