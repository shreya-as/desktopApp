    private void jButton_UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_UpdateActionPerformed
        // TODO add your handling code here:
        int id=Integer.parseInt(jTextField_Id.getText());
String name=jTextField_Name.getText();
double price =Double.parseDouble(jTextField_Price.getText());
int qty=Integer.parseInt(jTextField_Qty.getText());
String remarks=jTextArea_Remarks.getText();// TODO add your handling code here:

Product ob=new Product();
ob.setId(id);
ob.setName(name);
ob.setPrice(price);
ob.setQty(qty);
ob.setRemarks(remarks);

ProductDAO pDAO=new ProductDAO();
int count=pDAO.updateData(ob);//ob obj ma name,price sab set vara gako xa so ob pass gareko
//int count le kati row affect garo herxa
if(count>0)
{
    JOptionPane.showMessageDialog(null,"Record updated");
    clearFields();
    loadData();
}
     
                                           
    }//GEN-LAST:event_jButton_UpdateActionPerformed

